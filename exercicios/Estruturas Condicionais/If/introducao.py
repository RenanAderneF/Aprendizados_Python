# A estrutura condicional if é escrita com ":" no final da condição, e possuindo identação de bloco padrão.

height = 1.72

if height < 1.56:
    print("You're not tall enough to ride the giant wheel")

elif height < 1.60:
    print("You're not tall enough to ride the rollercoaster")

else:
    print("You're tall enougth to go on both toys")