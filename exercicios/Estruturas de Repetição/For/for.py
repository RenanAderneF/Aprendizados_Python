#Em python, podemos utilizar operadores de associação para percorrer facilmente coleções

primenumber = [0,1,3,7]

for number in primenumber:
    print(number)

dictanimals = {

    "mamal": "cow",
    "reptile": "lizard",
    "bird":  "cockatiel"
}

for animal in dictanimals.values():
    print(animal)