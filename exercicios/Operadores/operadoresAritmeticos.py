"""
+ (Adição): x + y 
- (Subtração): x - y
* (Multiplicação): x * y
/ (Divisão): x / y
// (Divisão para número de chão): x // y
% (Módulo ou Resto da divisão) x % y
** (Exponenciação): x ** y
"""

num1 = 25.16
num2 = 15.24

print(f"Soma: {num1 + num2}")
print(f"Subtração: {num1 - num2}")
print(f"Multiplicação: {num1 * num2}")
print(f"Divisão: {num1 / num2}")
print(f"Divisão de chão: {num1 // num2}")
print(f"Resto: {num1 % num2}")
print(f"Exponenciação: {num1 ** num2}")