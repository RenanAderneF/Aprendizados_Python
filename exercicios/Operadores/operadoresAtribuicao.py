"""
= (Atribuição direta): x = 5
+= (Somar ao valor): x += 5 ou x = x + 5
-= (Subtrair ao valor): x -= 5 ou x = x - 5
*= (Multiplica ao valor): x *= 5 ou x = x * 5
/= (Divide ao valor): x /= 5 ou x = x / 5
%= (Atribui o resto da divisão da variável com o valor fornecido): x %= 5 ou x = x % 5
...
"""

num1 = 20

num1 += 25
print(num1)

num1 -= 25
num1 *= 3
print(num1)

num1 /= 3
print(num1)

num1 %= 3
print(num1)