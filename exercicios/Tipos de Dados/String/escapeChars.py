# Para inserir caracteres "ilegais" em Python, utiliza-se "\" com o caractere a ser inserido.
# Por exemplo, é possível pôr uma palavra com aspas duplas em uma string de aspas duplas, o que seria proibido:

str1 = "Isso não poderia ser feito normalmente, porém eu \"descobri\" um jeito"

print(str1)

