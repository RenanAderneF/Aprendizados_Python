# Python é uma linguagem orientada à objetos, sendo seus tipos de dados definidos por classes.

# Para especificar como um tipo de dado deve se comportar, é necessário utilizar o processo de Casting. Ele é feito
# utilizando construtores, retornando objetos para inteiros, flutuantes e strings. Ex:

x = int(1)
y = float(4.32)
z = str(117)

# Variáveis em python não precisam ter seus tipos explicitamente declarados, podendo ter
# seus tipos modificados ao decorrer da execução do programa implícita ou explicitamente. Isso é chamado de casting.

altura = 150  # será do tipo inteiro
altura = 1.5  # será convertido para float

print(altura)

# Caso deseje-se declarar uma variável com seu tipo de dado explícito, podemos utilizar casting explicitamente. Ex:

idade = int(21)
print(idade)
